def sector_from_industry_code(industry_code: str | None) -> str:
    if not industry_code:
        return "미분류"
    digits = "".join(ch for ch in industry_code if ch.isdigit())
    if len(digits) < 2:
        return "미분류"
    return INDUSTRY_SECTOR_PREFIXES.get(digits[:2], "미분류")
